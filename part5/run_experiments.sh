#!/usr/bin/env bash
set -euo pipefail

RESULT_DIR="results"
SUMMARY_MD="${RESULT_DIR}/Part5_Experiment_Summary.md"
PATTERN_LIST=(1 4 8 16 32)
WARMUP_ITERS=20
TIMED_ITERS=200

mkdir -p "${RESULT_DIR}"
mkdir -p ../data

echo "[build] part5 default binaries"
make clean > "${RESULT_DIR}/build.txt" 2>&1
make >> "${RESULT_DIR}/build.txt" 2>&1

extract_value() {
    local pattern="$1"
    local file="$2"
    grep -m1 "^${pattern}" "${file}" | awk -F'= ' '{print $2}'
}

for patterns in "${PATTERN_LIST[@]}"; do
    data_file="../data/ofdm_input_multi_${patterns}.bin"
    run_log="${RESULT_DIR}/patterns_${patterns}_run.txt"

    echo "[data] patterns=${patterns}"
    ./generate_ofdm_multi "${data_file}" "${patterns}" > "${RESULT_DIR}/patterns_${patterns}_gen.txt" 2>&1

    echo "[run]  patterns=${patterns}"
    ./main "${data_file}" "${WARMUP_ITERS}" "${TIMED_ITERS}" > "${run_log}" 2>&1
done

{
    echo "# Part 5 Experiment Summary"
    echo
    echo "Generated by \`run_experiments.sh\`."
    echo
    echo "Timing in this file is GPU kernel-only timing collected from \`cudaEvent\` measurements inside \`main.cu\`."
    echo "It excludes input loading, allocation, H2D/D2H copies, and host-side verification."
    echo
    echo "## Pattern Sweep"
    echo
    echo "| Patterns | Verification | LS ms | LMMSE ms | Pipeline ms |"
    echo "| --- | --- | --- | --- | --- |"

    for patterns in "${PATTERN_LIST[@]}"; do
        run_log="${RESULT_DIR}/patterns_${patterns}_run.txt"
        verification="$(extract_value "Verification" "${run_log}")"
        ls_ms="$(extract_value "LS_KERNEL_MS" "${run_log}")"
        eq_ms="$(extract_value "LMMSE_KERNEL_MS" "${run_log}")"
        pipeline_ms="$(extract_value "PIPELINE_KERNEL_MS" "${run_log}")"

        echo "| ${patterns} | ${verification} | ${ls_ms} | ${eq_ms} | ${pipeline_ms} |"
    done

    echo
    echo "## Interpretation"
    echo
    echo "- Part 5 extends Part 4 from one OFDM pattern to multiple independent patterns."
    echo "- The main new mapping is \`blockIdx.y -> pattern index\`."
    echo "- This should expose more independent blocks and warps than Part 4, especially when the single-pattern workload is not enough to keep the GPU busy."
    echo "- Total pipeline time rises with pattern count because total work rises too. The more useful comparison is pipeline time per pattern, which drops as the GPU launch becomes better amortized."
    echo "- The measured trend should be interpreted together with PTXAS, PTX, and Nsight Compute data rather than from timing alone."
} > "${SUMMARY_MD}"

echo
echo "Wrote summary to ${SUMMARY_MD}"
