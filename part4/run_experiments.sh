#!/usr/bin/env bash
set -euo pipefail

INPUT_FILE="../data/ofdm_input.bin"
RESULT_DIR="results"
SUMMARY_MD="${RESULT_DIR}/Part4_Experiment_Summary.md"
WARMUP_ITERS=20
TIMED_ITERS=1000

mkdir -p "${RESULT_DIR}"

run_case() {
    local label="$1"
    local tpb_ls="$2"
    local tpb_eq="$3"
    local ls_mode="$4"
    local warmup_iters="$5"
    local timed_iters="$6"

    local build_log="${RESULT_DIR}/${label}_build.txt"
    local run_log="${RESULT_DIR}/${label}_run.txt"

    echo "[build] ${label} (TPB_LS=${tpb_ls}, TPB_EQ=${tpb_eq}, LS_MODE=${ls_mode})"
    make clean > "${build_log}" 2>&1
    make TPB_LS="${tpb_ls}" TPB_EQ="${tpb_eq}" >> "${build_log}" 2>&1

    echo "[run]   ${label}"
    ./main "${INPUT_FILE}" "${warmup_iters}" "${timed_iters}" "${ls_mode}" > "${run_log}" 2>&1
}

extract_value() {
    local pattern="$1"
    local file="$2"
    grep -m1 "^${pattern}" "${file}" | awk -F'= ' '{print $2}'
}

write_summary() {
    local best_ls_tpb=""
    local best_ls_pipeline=""
    local best_eq_tpb=""
    local best_eq_pipeline=""

    for label in ls_shared_64 ls_shared_128 ls_shared_256; do
        local run_log="${RESULT_DIR}/${label}_run.txt"
        local tpb_ls
        local pipeline_ms

        tpb_ls="$(extract_value "TPB_LS" "${run_log}")"
        pipeline_ms="$(extract_value "PIPELINE_KERNEL_MS" "${run_log}")"

        if [[ -z "${best_ls_pipeline}" ]] || awk "BEGIN { exit !(${pipeline_ms} < ${best_ls_pipeline}) }"; then
            best_ls_tpb="${tpb_ls}"
            best_ls_pipeline="${pipeline_ms}"
        fi
    done

    for label in eq_shared_128 eq_shared_256 eq_shared_512; do
        local run_log="${RESULT_DIR}/${label}_run.txt"
        local tpb_eq
        local pipeline_ms

        tpb_eq="$(extract_value "TPB_EQ" "${run_log}")"
        pipeline_ms="$(extract_value "PIPELINE_KERNEL_MS" "${run_log}")"

        if [[ -z "${best_eq_pipeline}" ]] || awk "BEGIN { exit !(${pipeline_ms} < ${best_eq_pipeline}) }"; then
            best_eq_tpb="${tpb_eq}"
            best_eq_pipeline="${pipeline_ms}"
        fi
    done

    {
        echo "# Part 4 Experiment Summary"
        echo
        echo "Generated by \`run_experiments.sh\`."
        echo
        echo "Timing in this file is GPU kernel-only timing collected from \`cudaEvent\` measurements inside \`main.cu\`."
        echo "It excludes input loading, allocation, H2D/D2H copies, and host-side verification."
        echo
        echo "## TPB Sweep for LS Shared Kernel"
        echo
        echo "| Case | TPB_LS | TPB_EQ | LS Mode | Verification | LS ms | LMMSE ms | Pipeline ms |"
        echo "| --- | --- | --- | --- | --- | --- | --- | --- |"

        for label in ls_shared_64 ls_shared_128 ls_shared_256; do
            local run_log="${RESULT_DIR}/${label}_run.txt"
            local tpb_ls
            local tpb_eq
            local ls_mode
            local verification
            local ls_ms
            local eq_ms
            local pipeline_ms

            tpb_ls="$(extract_value "TPB_LS" "${run_log}")"
            tpb_eq="$(extract_value "TPB_EQ" "${run_log}")"
            ls_mode="$(extract_value "LS_KERNEL_MODE" "${run_log}")"
            verification="$(extract_value "Verification" "${run_log}")"
            ls_ms="$(extract_value "LS_KERNEL_MS" "${run_log}")"
            eq_ms="$(extract_value "LMMSE_KERNEL_MS" "${run_log}")"
            pipeline_ms="$(extract_value "PIPELINE_KERNEL_MS" "${run_log}")"

            echo "| ${label} | ${tpb_ls} | ${tpb_eq} | ${ls_mode} | ${verification} | ${ls_ms} | ${eq_ms} | ${pipeline_ms} |"
        done

        echo
        echo "## TPB Sweep for LMMSE Kernel"
        echo
        echo "| Case | TPB_LS | TPB_EQ | LS Mode | Verification | LS ms | LMMSE ms | Pipeline ms |"
        echo "| --- | --- | --- | --- | --- | --- | --- | --- |"

        for label in eq_shared_128 eq_shared_256 eq_shared_512; do
            local run_log="${RESULT_DIR}/${label}_run.txt"
            local tpb_ls
            local tpb_eq
            local ls_mode
            local verification
            local ls_ms
            local eq_ms
            local pipeline_ms

            tpb_ls="$(extract_value "TPB_LS" "${run_log}")"
            tpb_eq="$(extract_value "TPB_EQ" "${run_log}")"
            ls_mode="$(extract_value "LS_KERNEL_MODE" "${run_log}")"
            verification="$(extract_value "Verification" "${run_log}")"
            ls_ms="$(extract_value "LS_KERNEL_MS" "${run_log}")"
            eq_ms="$(extract_value "LMMSE_KERNEL_MS" "${run_log}")"
            pipeline_ms="$(extract_value "PIPELINE_KERNEL_MS" "${run_log}")"

            echo "| ${label} | ${tpb_ls} | ${tpb_eq} | ${ls_mode} | ${verification} | ${ls_ms} | ${eq_ms} | ${pipeline_ms} |"
        done

        echo
        echo "## Shared vs Serial LS Comparison"
        echo
        echo "| Case | TPB_LS | TPB_EQ | LS Mode | Verification | LS ms | LMMSE ms | Pipeline ms |"
        echo "| --- | --- | --- | --- | --- | --- | --- | --- |"

        for label in ls_shared_256 ls_serial_256; do
            local run_log="${RESULT_DIR}/${label}_run.txt"
            local tpb_ls
            local tpb_eq
            local ls_mode
            local verification
            local ls_ms
            local eq_ms
            local pipeline_ms

            tpb_ls="$(extract_value "TPB_LS" "${run_log}")"
            tpb_eq="$(extract_value "TPB_EQ" "${run_log}")"
            ls_mode="$(extract_value "LS_KERNEL_MODE" "${run_log}")"
            verification="$(extract_value "Verification" "${run_log}")"
            ls_ms="$(extract_value "LS_KERNEL_MS" "${run_log}")"
            eq_ms="$(extract_value "LMMSE_KERNEL_MS" "${run_log}")"
            pipeline_ms="$(extract_value "PIPELINE_KERNEL_MS" "${run_log}")"

            echo "| ${label} | ${tpb_ls} | ${tpb_eq} | ${ls_mode} | ${verification} | ${ls_ms} | ${eq_ms} | ${pipeline_ms} |"
        done

        echo
        echo "## Interpretation"
        echo
        echo "- Larger \`TPB_LS\` does not automatically improve timing. In the current LS sweep, \`TPB_LS=${best_ls_tpb}\` gives the lowest measured pipeline kernel-only time."
        echo "- The LMMSE sweep is also non-monotonic. In the current measurements, \`TPB_EQ=${best_eq_tpb}\` is the best among the tested cases."
        echo "- The shared-vs-serial comparison should be read as a comparison between:"
        echo "  - block-cooperative shared-memory reduction"
        echo "  - one-thread-per-subcarrier serial Stage 1"
        echo "  It is not evidence that shared memory alone explains the full timing gap."
    } > "${SUMMARY_MD}"
}

run_case "ls_shared_64" 64 256 shared "${WARMUP_ITERS}" "${TIMED_ITERS}"
run_case "ls_shared_128" 128 256 shared "${WARMUP_ITERS}" "${TIMED_ITERS}"
run_case "ls_shared_256" 256 256 shared "${WARMUP_ITERS}" "${TIMED_ITERS}"

run_case "eq_shared_128" 256 128 shared "${WARMUP_ITERS}" "${TIMED_ITERS}"
run_case "eq_shared_256" 256 256 shared "${WARMUP_ITERS}" "${TIMED_ITERS}"
run_case "eq_shared_512" 256 512 shared "${WARMUP_ITERS}" "${TIMED_ITERS}"

run_case "ls_serial_256" 256 256 serial "${WARMUP_ITERS}" "${TIMED_ITERS}"

write_summary

echo
echo "Wrote summary to ${SUMMARY_MD}"
